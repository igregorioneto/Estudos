#include <iostream>

using namespace std;

int main() {
    // int
    int value;
    value = 5;
    cout << value << endl;

    // float
    float real;
    real = 5.2;
    cout << real << endl;

    // double
    double real2;
    real2 = 5.2e99;
    cout << real2 << endl;

    // char
    char character;
    character = 'b';
    cout << character << endl;

    // string
    string word;
    word = "soccer";
    cout << word << endl;

    // exercice
    int age;
    cout << "Enter your age? " << endl;
    cin >> age;
    cout << "Age: " << age << endl;


    return 0;
}